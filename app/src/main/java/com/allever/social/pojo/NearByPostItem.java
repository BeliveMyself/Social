public class NearByPostItem  {

    private String id;
    private String postname;
    private double distance;
    private String salary;
    private String user_id;
    private String user_head_path;
    private String recruit_id;
    private int is_owner;
    private String phone;
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getPostname() {
        return postname;
    }
    public void setPostname(String postname) {
        this.postname = postname;
    }
    public double getDistance() {
        return distance;
    }
    public void setDistance(double distance) {
        this.distance = distance;
    }
    public String getSalary() {
        return salary;
    }
    public void setSalary(String salary) {
        this.salary = salary;
    }
    public String getUser_id() {
        return user_id;
    }
    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }
    public String getUser_head_path() {
        return user_head_path;
    }
    public void setUser_head_path(String user_head_path) {
        this.user_head_path = user_head_path;
    }
    public String getRecruit_id() {
        return recruit_id;
    }
    public void setRecruit_id(String recruit_id) {
        this.recruit_id = recruit_id;
    }
    public int getIs_owner() {
        return is_owner;
    }
    public void setIs_owner(int is_owner) {
        this.is_owner = is_owner;
    }
    public String getPhone() {
        return phone;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }




}
